def style_heading(p, text, size=16):
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(size)

def style_body(p, text, size=12, bold=False, italic=False):
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.bold = bold
    run.italic = italic

def add_slide(doc, num, title, body):
    p = doc.add_paragraph()
    style_heading(p, f"–°–õ–ê–ô–î {num}: {title}", 14)
    doc.add_paragraph("")
    for line in body.strip().split("\n"):
        p = doc.add_paragraph()
        style_body(p, line, 12)
    doc.add_paragraph("")
    doc.add_paragraph("‚îÄ" * 50)
    doc.add_paragraph("")

