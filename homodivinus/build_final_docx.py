def add_styled_para(doc, text, size=12, bold=False, italic=False, align=None):
    p = doc.add_paragraph()
    if align:
        p.alignment = align
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.bold = bold
    run.italic = italic
    return p

def add_card(doc, num, total, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(f"‚Äî {num}/{total} ‚Äî")
    run.bold = True
    run.font.size = Pt(11)
    
    for line in text.strip().split("\n"):
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run(line)
        run.font.size = Pt(12)
    doc.add_paragraph("")

def section_break(doc, title):
    doc.add_paragraph("")
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run("‚ïê" * 40)
    run.font.size = Pt(10)
    doc.add_heading(title, 1)
    doc.add_paragraph("")

